package biblioteca;

/**
 * La clase <code>EstadoCopia</code> define el estado de una copia de un libro en la biblioteca
 * @author Marcus Skelly
 * @version 1.0
 * @since 1.0
 */

public enum EstadoCopia {

    /**
     * Estados en los que una copia se encuentra
     */
    PRESTADO,
    REAPARICION,
    BIBLIOTECA,
    RETRASO
}
